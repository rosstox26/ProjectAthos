public class Review {
        String rating;
        String title;
        String review;
        String visit;
        String quality;
        String comfort;
        String service;


        public Review () {

        }

        public Review (String rating, String title, String review, String visit, String quality, String comfort, String service ) {
            this.rating = rating;
            this.title = title;
            this.review = review;
            this.visit = visit;
            this.quality = quality;
            this.comfort = comfort;
            this.service = service;


        }
}
